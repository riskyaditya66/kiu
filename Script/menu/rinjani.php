				<p>Jalur pendakian Torean hanya dikenal bagi para pendaki lokal yang ingin melakukan ritual adat atau keagamaan di Danau Segara Anak. Dibandingkan jalur lainnya, sebenarnya jalur mempunyai karakteristik yang tidak dimiliki jalur lain. Pendakian melalui jalur Torean merupakan pendakian melewati lembah dan punggung menuju Danau Segara Anak. Dibandingkan jalur lain jalur ini tidak terlalu curam, hanya saja kanan atau kiri trail merupakan jurang sehingga perlu ekstra hati-hati. Obyek yang bisa diamati sepanjang perjalanan seperti Air Terjun Penimbungan, Air Terjun Pancor Mas, Gua Susu dengan kolam hangatnya,
